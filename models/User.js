const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const SignupSchema = new Schema ({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    register_date: {
        type: Date,
        default: Date.now
    }
})

module.esports = Signup = mongoose.model('signup', SignupSchema);